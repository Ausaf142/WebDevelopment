let a= prompt("Enter the number for Day");
a=Number.parseInt(a);
switch (a){
    case 0:
        break;
    case 1:
        alert("Mon")
        break;
    case 2:
        alert("tue")
        break;
    case 3:
        alert("Wed")
        break;
    case 4:
        alert("Thu")
        break;
    case 5:
        alert("Fri")
        break;
    case 6:
        alert("Sat")
        break;
    default:
        alert("Not Valid")
        console.log("Not valid")
               
}

